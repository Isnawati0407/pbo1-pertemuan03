package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch(jalanJalan){
            case 1:
                System.out.println("urang lapas");
                break;
            case 2:
                System.out.println("urang acayan");
                break;
            case 3:
                System.out.println("ubat curuna");
                break;
            case 4:
                System.out.println("iwak pakasam");
                break;
            default:
                System.out.println("hantu banyu");
                break;
        }
    }
}
